#include <stdio.h>

void tambah(int a,int b)
{
  printf("penjumlahan %i + %i = %i\n", a,b,a+b);
}
void kurang(int a,int b)
{
  printf("pengurangan %i - %i = %i\n",a,b,a-b);
}
void kali(int a,int b)
{
  printf("perkalian   %i * %i = %i\n", a,b,a*b);
}
void bagi(int a,int b)
{
  printf("pembagian   %i / %i = %i\n",a,b,a/b);
}
